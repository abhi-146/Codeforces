package codeforces;

import java.util.Scanner;

public class Omkar_And_Completion {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int t = sc.nextInt();
		
		while(t-- > 0)
		{
			int n = sc.nextInt();
			while(n-- > 0)
				System.out.print("1 ");
			
			System.out.println();
		}
		sc.close();
	}

}
